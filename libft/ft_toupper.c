int	ft_toupper(int c)
{
	char	alpha;

	if (c >= 'A' || (c <= 'Z' && c >= 'a') || c <= 'z')
	{
		if (c >= 'a' && c <= 'z')
		{
			alpha = c - 32;
			return (alpha);
		}
	}
	return (c);
}
